//===============================FOR LOOP==================================

// Print 1 to 5 using for loop

{
  let i;
  for (i = 1; i <= 5; i++) {
    console.log(i);
  }
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

// print 5 to 1 using for loop

{
  let aga;
  for (aga = 5; aga >= 1; aga--) {
    console.log(aga);
  }
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//===============================WHILE LOOP=================================

// print 1 to 5 using while loop

{
  let i = 1;
  while (i <= 5) {
    console.log(i);
    i++;
  }
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

// print 5 to 1 using while loop

{
  let i = 5;
  while (i >= 1) {
    console.log(i);
    i--;
  }
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//===============================DO-WHILE LOOP===========================

// print 1 to 5 using do-while loop

{
  let i = 1;
  do {
    console.log(i);
    i++;
  } while (i <= 5);
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

// print 5 to 1 using do-while loop

{
  let i = 5;
  do {
    console.log(i);
    i--;
  } while (i >= 1);
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);
