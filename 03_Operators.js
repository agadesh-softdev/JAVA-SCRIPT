// Operators


// 01 ARITHMETIC OPERATOR

// ADDITION
{
  let a = 10;
  let b = 20;
  let c = a + b;
  console.log(c);
}

//MULTIPLICATION
{
  let a = 30;
  let c = 10;
  console.log(a - c);
}

// DIVISION
{
  let a = 20;
  let c = 2;
  console.log(a / c);
}

//MODULUS
{
  let a = 11;
  let b = 2;
  console.log(a % b);
}

//EXPONENTIATION
{
  let a = 10;
  let b = 2;
  console.log(a ** b);
}
