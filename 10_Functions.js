// FUNCTION DECLARATION

agadesh("agi"); // We call the function before or after the code
function agadesh(name) {
  console.log("Hii" + " " + name);
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//-----------------------------------------------------------------------------------------------------------------------------------------------

// ARROW FUNCTION

{
  const agadesh = (name) => {
    console.log("Hii " + name);
  };
  agadesh("agi");
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//-----------------------------------------------------------------------------------------------------------------------------------------------

// FUNCTION WITH RETURN VALUE

{
  function agadesh(a, b) {
    return a + b;
  }
  let result = agadesh(1, 2);
  console.log(result);
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//-----------------------------------------------------------------------------------------------------------------------------------------------

// DEFAULT PARAMETERS

{
  function agadesh(name = "agadesh") {
    console.log("Hii " + name);
  }
  agadesh();
  agadesh("agi");
}

console.log(
  "-----------------------------------------------------------------------------------------------------------"
);

//-----------------------------------------------------------------------------------------------------------------------------------------------

// FUNCTION WITH REST PARAMETERS

function agadeshwaran(...store) {
  i = 0;
  for (let a of store) {
    i += a;
  }
  return i;
}
const result = agadeshwaran(5, 10);
console.log(result);
